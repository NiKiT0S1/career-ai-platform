package com.careerai.backend.channel;

/**
 * Результат попытки распознать дату в тексте.
 */

public enum DateParseStatus {
    PARSED,  // Корректная календарная дата найдена
    UNKNOWN,  // Точная дата отсутствует или сформулирована неопределённо
    INVALID  // Найдена похожая на дату конструкция, но календарно она невозможна
}

package com.careerai.backend.channel;

/**
 * Тип пользовательского запроса к базе знаний и постам Telegram-канала.
 */

public enum ChannelSearchIntent {
    GENERAL_CHAT, // Обычное общение, которое не требует поиска по постам канала.
    VACANCY, // Вопрос про вакансии, работу, стажировки или позиции.
    PRACTICE, // Вопрос про производственную практику, документы, договоры или оформление.
    DEADLINE, // Вопрос про дедлайны, сроки, даты сдачи или временные рамки.
    GENERAL_UPDATES, // Вопрос про свежие объявления или новости из Telegram-канала.
    UNKNOWN, // Не удалось уверенно определить тип запроса.

    FAQ // Вопрос из будущей базы частых вопросов.
}

package com.careerai.backend.channel;

/**
 * Тип Telegram-поста после структурированного анализа.
 *
 * Нужен, чтобы backend мог отличать вакансии, практику,
 * дедлайны, мероприятия и обычные объявления.
 */

public enum TelegramChannelPostType {
    VACANCY, // Пост с вакансией, стажировкой или предложением работы
    PRACTICE, // Пост про производственную практику, документы, сроки или оформление
    EVENT, // Пост про мероприятие, встречу, ярмарку вакансий или workshop
    DEADLINE, // Пост, где главным смыслом является дедлайн или срок
    ANNOUNCEMENT, // Общее объявление без узкой категории
    OTHER // Тип не удалось определить
}

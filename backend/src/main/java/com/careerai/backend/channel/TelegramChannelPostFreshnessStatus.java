package com.careerai.backend.channel;

/**
 * Показывает, насколько актуален Telegram-пост
 * для использования в ответах CareerAI.
 *
 * Архивация хранится отдельно, потому что пост может быть
 * актуальным или истёкшим независимо от ручного архивирования.
 */

public enum TelegramChannelPostFreshnessStatus {
    ACTIVE,  // Публикация считается актуальной
    UNKNOWN,  // Актуальность нельзя уверенно определить. Например, у вакансии отсутствует точный дедлайн
    EXPIRED,  // Срок публикации или события уже прошёл
    INVALID  // В публикации обнаружены некорректные данные. Например, невозможная дата
}

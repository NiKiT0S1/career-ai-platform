package com.careerai.backend.channel;

/**
 * Показывает, входит ли найденная календарная дата
 * в период действия публикации.
 */

public enum DateBoundaryType {

    /**
     * Публикация перестаёт действовать
     * в начале указанной даты.
     *
     * Пример: "до 21 июля".
     */
    EXCLUSIVE,

    /**
     * Публикация действует весь указанный день.
     *
     * Примеры: "по 21 июля", "до конца 21 июля".
     */
    INCLUSIVE,

    /**
     * В тексте нет явного указания границы.
     *
     * Для дедлайнов backend применяет безопасное
     * предметное правило: дата считается включительной.
     */
    UNSPECIFIED
}
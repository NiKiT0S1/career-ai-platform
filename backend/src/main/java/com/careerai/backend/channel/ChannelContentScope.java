package com.careerai.backend.channel;

/**
 * Ограничивает категории Telegram-постов,
 * которые допустимо использовать для ответа.
 *
 * В отличие от свободного поля topic,
 * scope имеет строгий и предсказуемый набор значений.
 */

public enum ChannelContentScope {
    NONE,  // Telegram-посты для ответа не требуются
    ALL_UPDATES,  // Пользователь запросил любые свежие публикации
    VACANCIES,  // Вакансии, стажировки и предложения работы
    EVENTS,  // Мероприятия, встречи, мастер-классы и их обновления
    PRACTICE,  // Производственная практика и связанные объявления
    DEADLINES,  // Сроки и дедлайны разных категорий
}
